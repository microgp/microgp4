import logging

__all__ = ['setup_logging']

logger = logging.getLogger(__name__)


def setup_logging(level=logging.INFO):
    # %(pathname)s:%(lineno)d
    logging.basicConfig(
        force=True,
        level=level,
        format='%(asctime)s %(levelname)s: %(message)s [%(name)s]',
        datefmt='%H:%M:%S',
    )
    logger.debug(f"Logging initialized")
